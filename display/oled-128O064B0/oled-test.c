int main(int argc, char **argv)
{
	/* Initialize OLED */
	if (oledphy_init()) {
		LOG_ERROR("oled phy init failed.");
		return -1;
	}

	TRACE("initialized phy.");

	/* Set column start and end address */
	oledphy_sendCmd(0x21);
	oledphy_sendCmd(0x00);
	oledphy_sendCmd(0x7F);

	int y = 0, offset = 0, inverted = 0;

	for (y = 0; y < Y_RES; ++y) {
		oledphy_sendCmd(y | 0xb0);
		oledphy_sendDataBuf((uint8_t *)&(logo[X_RES * y]), X_RES);
	}

	while (1)
	{
		/* Offset logo up by one row */
		++offset;
		offset = (offset) % (Y_RES * 8);
		oledphy_sendCmd(0xD3);
		oledphy_sendCmd((uint8_t)offset);

		usleep(DELAY_US);

		/* Invert colors */
		if (offset == 0) {
			if(inverted)
				oledphy_sendCmd(0xA6);
			else
				oledphy_sendCmd(0xA7);

			inverted = !inverted;
		}
	}

	return 0;
}
